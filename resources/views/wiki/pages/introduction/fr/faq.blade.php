<h2>faq</h2>

<p>Windjammers est un jeu plutôt simple qui ne nécessite pas vraiment la lecture d'un tel wiki pour commencer.</p>

<p>Cela dit, de nombreux débutants se posent les questions suivantes.</p>

<h3>C'est quoi la tier list ? Je veux gagner sans faire d'effort avec un perso pété !</h3>

<p>Et bien il n'y a pas vraiment de tier list pour Windjammers, il n'y a pas de notion de match-up en fait.</p>

<p>Quelques personnages ont des facilités pour certaines choses, comme des tirs courbés qui marchent mieux sur certains terrains, mais pas de quoi faire une différence notable pour les séparer en "tier".</p>

<p>Il est surtout question de style de jeu et d'affinités personnelles.</p>

<h3>Ok... bon du coup : quel est le meilleur perso pour débuter ?</h3>

<p>Dans le jeu Neo Geo d'origine, Mita et Miller portent l'indication "For Beginners", Scott et Wessel "For experts". Nous avons demandé à ce que ce soit changé par "Léger/Moyen/Lourd" dans les portages de Dotemu, ce qui décrit plus précisement ces persos et n'induit pas en erreur : en effet, Mita est plutôt considérée comme étant la plus difficile à maîtriser.</p>

<p>Ceci étant dit, voici quelques pistes pour vous aider à trouver un personnage avec qui débuter :</p>

<ul>
<li>éssayez-les tous, s'il y en a un qui vous plaît ou avec lequel le jeu vous semble plus fun, gardez-le ;
<li>si vous êtes indécis et que vous hésitez en défense, essayez Wessel et rester sur votre ligne de fond de court ;</li>
<li>si vous êtes indécis mais que vous n'êtes pas du genre patient et préférez bouger dans tous les sens, prenez plutôt Miller ;</li>
<li>si vous êtes toujours indécis, prenez Costa. C'est le personnage le plus neutre du cast.</li>
</ul>
<p>Évidemment, n'hésitez pas à changer pour un personnage qui s'adapte mieux au style de jeu que vous développerez par la suite.</p>

<h3>Quelle est la meilleure version - où trouve t-on le plus de joueurs ?</h3>

<p>La plupart des joueurs se rencontrent sur FightCade car même s'il s'agit proablement de la façon de jouer la moins agréable, c'est "gratuit" et c'est bien plus pratique pour organiser et streamer des tournois en ligne.</p>

<p>À l'inverse, il n'y a pas mieux que de jouer sur la version Neo Geo d'origine, surtout sur une borne d'arcade japonaise. Mais là c'est cher et pas question de jouer en ligne.</p>

<p>Les portages sur PS4 et Switch sont très bons mais vous ne rencontrerez presque personne en matchmaking. Pour trouver des joueurs il vaut mieux rejoindre le Discord tenu par la communauté américaine et se signaler dans le channel #looking-for-match.</p>