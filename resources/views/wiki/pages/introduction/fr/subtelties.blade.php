<h2>Quelques subtilités</h2>

<p>Windjammers est un jeu simple mais qui n'est pas sans subtilités, parfois cachées.</p>

<h3>Mécaniques de "maintien de la hype"</h3>

<p>Car dans le cas de Windjammers, il ne s'agit pas uniquement de permettre aux joueurs en difficulté de revenir dans le match.</p>

<p>Pour cela, plus l'écart de points est élevé plus le timing pour envoyer des supersonic shots ou renvoyer des supers attaques sera permissif.</p>

<p>Mais la même chose se produit - indépendemment du score - lors de la réception de supersonic shots : pour ne pas casser le rhythme de l'échange, il est facile de renvoyer un supersonic shot suite à un supersonic shot.</p>

<h3>Décalage au service</h3>

<p>La position à laquelle vous commencez un engagement varie de quelques pixels horizontalement et surtout verticalement.</p>

<p>Chaque service est donc plus ou moins différent.</p>

<p>Cela permet de s'assurer qu'un personnage n'a pas un service garanti et qu'il puisse le répéter à chaque fois.</p>

<p>Et de la même manière, un personnage pourra avoir accès à un meilleur service de temps à autre, notamment sur les terrains à bumpers où une même manipulation peut envoyer le disque taper, frôler ou éviter l'obstacle.</p>

<h3>Le bug de Miller</h3>

<p>Ce bug n'est pas un bug à proprement parlé puisqu'il est plutôt dû à une erreur de design que de programmation. Aussi, s'il arrive plus souvent avec Miller, Biaggi et Wessel peuvent également être affectés. Mais on va quand même l'appeler le "bug de Miller", faute de mieux...</p>

<p>Lors d'un toss, le frisbee est en l'air et si on se trouve en dessous, notre avatar commence à charger pour pouvoir lancer une super dès que le disque nous retombe dans les mains. Et on ne peut pas annuler cette phase de charge.</p>

<p>Le problème du "bug de Miller" c'est que le disque peut ne pas retomber sur le personnage mais à côté, sur le sol. L'adversaire remporte donc 2 points comme pour un lob alors que votre Miller ne pouvait pas attrapper le disque puisqu'il était en train de charger, en vain.</p>

<p>Le bug se produit à cause d'un changement dans la hitbox de ces 3 personnages entre le toss et la charge. Miller intercepte le frisbee avec son toss à la hitbox avantageuse et se retrouve verrouillé en position de charge mais le frisbee, lui, se trouve en dehors sa hitbox de charge qui elle est plus petite.</p>

<p>En jeu, ça n'arrive que rarement et presqu'uniquement lors d'un toss vers le haut. On considère que cela fait partie intégrante du jeu et donc on ne rejoue pas le point si cela se produit.</p>

<h3>Le bug du scoring continu</h3>

<p>Encore plus rare et toujours inexpliqué.</p>

<p>Un joueur marque et soudainement le jeu accorde 5 points à l'adversaire, puis quelques secondes après encore 5 points, et ce jusqu'à la fin de la manche.</p>

<p>Si ce bug arrive en tournoi, on rejoue le round.</p>

<h3>But contre son camp</h3>

<p>Il est possible de marquer dans son propre camp. C'est très rare mais ça peut se produire de diverses manières.</p>

<p>La super de Biaggi sur Clay peut faire des cercles très amples jusqu'à toucher la zone d'enbut du lanceur.</p>

<p>Encore plus rare : au fin fond de ses propres cages, effectuer une super spin qui boucle en arrière peut également produire un cercle qui passe par l'enbut.</p>

<h3>Bonus games</h3>

<p>Il existe 2 jeux bonus dans Windjammers en mode solo mais jouables indépendemment sur les portages de Dotemu.</p>

<h4>Dog Distance, "le jeu du chien"</h4>

<p>Après avoir triomphé de vos 2 premiers adversaires, vous serez de retour sur une plage avec votre fidèle canidé pour tenter de lui faire rattrapper le frisbee le plus loin possible en le faisant sauter au dessus des plagistes.</p>

<p>Le choix du personnage n'a pas d'importance, tous lanceront à la même force dans cette épreuve. De toute façon pour faire un bon score il faudra sortir un supersonic shot, éviter les obstacles et sauter en toute fin pour que le chien attrappe le disque au dernier moment et profite de l'allonge de tout son saut.</p>

<p>Plus grande distance jamais vue : 221 mètres</p>

<h4>Bowling</h4>
<p>Après 4 personnages battus, vous ferez un détour par le bowling, pour faire du bowling mais avec votre frisbee.</p>

<p>Le temps est limité donc vous voudrez renvoyer le disque le plus tôt possible et tenter des supersonic shots.</p>

<p>Pour le reste c'est comme au bowling, des spares et des strikes, avec une chance de plus si vous strikez au round 10.</p>

<p>L'intérêt est limité, votre aptitude au lancer de frisbee ne vous permettra pas de sauver certains spares avec des quilles restantes sur des côtés opposés.</p>

<p>Surtout, tous les personnages ne sont pas égaux et Costa enchaînera les strikes avec une facilité déconcertante, par exemple.</p>