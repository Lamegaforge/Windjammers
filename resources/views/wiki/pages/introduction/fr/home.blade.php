<h2>Windjammers, ou Flying Power Disc au Japon, est un jeu vidéo développé par Data East et sorti en 1994 sur Neo Geo.<h2>

<h3>Concept et gameplay</h3>
<p>On peut rapprocher Windjammers du tennis ou encore de l'illustre Pong sauf qu'il est ici question de frisbee.</p>

<p>Deux joueurs sont face à face chacun dans leur moitié de terrain et doivent envoyer le frisbee dans la zone d'enbut adverse.</p>

<p>Très facile à comprendre et à prendre en main, le jeu ne nécessite que 2 boutons d'actions.</p>

<p>Les six personnages jouables présentent des caractéristiques légèrement différentes mais peuvent tous se mouvoir dans leur partie de terrain, faire des glissades ("dash") et intercepter le disque quand ils ne l'ont pas déjà en main.</p>

<p>S'ils ont la possession du frisbee, ils ne peuvent alors plus bouger et doivent lancer le disque soit en ligne droite soit avec de l'effet, tenter des lobs ou même effectuer diverses attaques spéciales dans certaines conditions.</p>

<p>L'enbut est découpé en zones valant 3 (jaune) ou 5 (rouge) points reparties différemment selon le terrain choisit. Si le disque tombe au sol, seuls 2 points sont accordés.</p>

<h3>Conditions de victoire</h3>
<p>Un match se joue en deux manches gagnantes.</p>

<p>Avec les réglages utilisés en compétition*, une manche se gagne en étant le premier à 12 points ou en menant au score à l'issue de 99 secondes de jeu.</p>

<p>En cas d'égalité, les deux joueurs se voient accorder une manche. Si l'égalité intervient alors que les joueurs sont déjà à une manche partout, la mort subite intervient : une ultime manche de 30 secondes durant laquelle le premier joueur à marquer gagne.</p>

<p>* les réglages par défaut sont de 12 points et 30 secondes sauf au Japon avec 15 points et 45 secondes. Toutefois il est probable que les développeurs envisageaient des manches de 60 ou 90 secondes avant de raccourcir la durée pour forcer les joueurs à remettre des crédits dans la borne et satisfaire les gérants des salles d'arcade à l'époque.</p>

<h3>Portages</h3>
<p>Le jeu sort donc sur Neo Geo MVS (version arcade) en février 1994 puis sur Neo Geo AES (version console) en avril et enfin sur Neo Geo CD en janvier 1995.</p>

<p>Il faudra attendre 2017 pour voir le jeu porté sur d'autres support grâce à Dotemu. D'abord sur PS4 et PSVita puis l'année suivante sur Switch. Cette version reprend le jeu tel quel avec des nouveaux menus et surtout le jeu en ligne.</p>

<p>Windjammers est également beaucoup joué en émulation sur ordinateur, notamment via FightCade qui permet également des affrontements en ligne.</p>