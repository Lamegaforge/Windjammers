<h2>faq</h2>

<p>Windjammers is a rather simple game at the core, so you might not need to go through such a wiki to start on your own.</p>

<p>However most newcomers often ask the following questions.</p>

<h3>What's the tier list? Gimme a character that can make me win effortlessly!</h3>
<p>There's no real tier list in Windjammers, you can hardly speak of match-ups as well.</p>

<p>Some characters might have an easier time with certain things, like curves that works well on a given stage, but the gap is not high enough to separate them into different tiers.</p>

<p>It all boils down to personal affinities and playstyle.</p>

<h3>But... let me rephrase: which character is better suited for beginners? </h3>
<p>On the original Neo Geo version, both Mita and Miller are marked as "For Beginners", Scott and Wessel are labelled "For Experts". We insisted that Dotemu change this to "Light/Medium/Heavy" in the newest ports of the game, which describes each character a lot better whereas the former mention is misleading. Mita is even considered the most difficult character to master.</p>

<p>That being said, here's a quick guide to help you pick a character:</p>
<ul>
<li>Try all characters, see if you can spot one that you simply like best or that makes the game more fun to play for you</li>
<li>If you're undecided and you feel insecure when receiving the disc, try Wessel and stay on your defense line</li>
<li>If you're undecided but you lack patience and can't stay in place then try Miller</li>
<li>If you're still undecided, pick Costa. He's the most neutral character of the bunch</li>
</ul>
<p>And then with time, change according to the playstile you've developed.</p>