<h2>Windjammers, or Flying Power Disc in Japan, is a video game developed by Data East in 1994 for the Neo Geo.</h2>

<h3>Concept & gameplay</h3>
<p>Windjammers can somewhat be compared to tennis or even to one of the first video game based on that sport, Pong. Here though, we're dealing with frisbees.</p>

<p>Two players face each other on a field divided in two by a net. They have to sent the frisbee past their opponent to score.</p>

<p>Windjammers is a two-button game, very easy to understand and to play.</p>

<p>You can select one of six characters. They all control the same but have slightly different statistics.</p>

<p>When they are not holding the disc, they are free to move in their respective side of the arena and prepare for a catch.</p>

<p>When they do hold the frisbee, players can no longer move and must throw the disc with straight, curved or lob shots and even go for a special attack if ready.</p>

<p>The scoring zone is divided into sections worth either 3 (yellow) or 5 (red) points, dispatched differently depending on the stage. Only two points are awarded if the disc falls flat on the ground.</p>

<h3>Win condition</h3>
<p>Matches employ a first-to-two (best-of-three) round format.</p>

<p>When playing competitively*, a match ends when one of the players reach 12 points or by leading until the 99 seconds of game time are over.</p>

<p>In case of a draw, both players win a round. If the score was already 1 -1, a sudden death occurs: a 30 seconds round during which the first player to score is declared the winner.</p>

<p>* default settings are 12 points and 30 seconds, except in Japan where the game defaults to 15 points and 45 seconds. However it's almost certain that the developers wanted matches to last 60 or 90 seconds but had to limit that time so that arcade owners see more profit by having players to frequently insert more coins.</p>

<h3>Ports</h3>

<p>The game was initally released on the Neo Geo MVS (arcade) in February 1994 and then on the Neo Geo AES (console) in April, and finally on the Neo Geo CD in January 1995.</p>

<p>23 years later, Dotemu brought the game to newer systems. First on PS4 and PSVita in 2017 and a year later on the Nintendo Switch. This port reprises the game as is, and adds new menus and an online mode.</p>

<p>Windjammers is also played on PC through emulation, most commonly on FightCade since it allows online play.</p>