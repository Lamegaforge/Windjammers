<h2>Concrete</h2>

<p>Où l'on découvre non sans suprise que le béton, ça peut faire très mal.</p>

<p>Car ce grand terrain, en plus d'une disposition des points inversée (une zone jaune à 3 points au centre et deux zones rouges à 5 points aux bords), propose de jouer avec des obstacles : 2 bumpers situés au niveau du filet (par conséquent plus large) proches des bords du terrain.</p>

<p>En plus d'offrir une chance de plus de prendre son adversaire à contre-pied, un rebond favorable sur ces bumpers peut faire arriver le frisbee directement dans la zone des 5 points ! Certains personnages peuvent s'en servir dès le service, comme Wessel.</p>

<p>Il faudra garder son calme et se familiariser avec la physique des bumpers pour pouvoir rester serein en défense et agrandir son panel d'attaques.</p>

<p>La musique de ce terrain s'intitule "You got a power" et comporte de vraies paroles et des solos bien sentis !</p>