<h2>Carrelage</h2>

<p>Oui, carrelage, enfin c'est la traduction française sur les portages. On préfère "synthétique" même si en vrai on en sait trop rien.</p>

<p>Tiled c'est comme Beach mais en moins exotique et surtout une disposition des points inversée, avec la zone jaune à 3 points au centre et deux zones rouges à 5 points (plus petites) dans les coins de l'enbut.</p>

<p>Par conséquent les personnages qui peuvent facilement cibler les coins opposés seront d'autant plus dangereux. C'est pour cette raison qu'on associe souvent Steve Miller à Tiled : sa super custom a beau être prévisible, vu qu'elle colle aux bords du terrain elle finira forcément en 5 points si elle passe.</p>

<p>En mode solo, il est l'un des 2 terrains sur lesquels le premier match peut avoir lieu (avec Beach).</p>