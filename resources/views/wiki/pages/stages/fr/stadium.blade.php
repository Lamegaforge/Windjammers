<h2>Stade</h2>

<p>Les projecteurs s'allument sur des gradins remplis d'une foule enthousiaste à l'idée d'assister à l'affrontement final...</p>

<p>Car c'est sur Stadium que le dernier match du mode solo prend place.</p>

<p>C'est un grand terrain avec une disposition des points... en apparence classique : la zone rouge à 5 points au centre de l'enbut est miniscule au début du match mais s'agrandit en la défaveur du joueur menant au score.</p>

<p>Cependant si le joueur mené aux points marque, la taille de la zone rouge adverse peut être remise à zéro. On peut ainsi volontairement laisser passer un 3 points à la fin d'un set rondement mené pour réinitialiser la zone au début de la prochaine manche.</p>

<p>Celà rajoute de la tension et un brin de stratégie par rapport à Lawn. En outre, les débutants apprécient généralement Stadium grâce à cette mécanique de "comeback" bien visible.</p>